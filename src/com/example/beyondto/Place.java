package com.example.beyondto;

public class Place { 
	
	private String nomeLuogo, descrizioneLuogo, proprietaFazione, statoLuogo;
	private double latNE, lngNE, latSO, lngSO;
		
	public Place(){}

	public String getNomeLuogo() {
		return nomeLuogo;
	}


	public void setNomeLuogo(String nomeLuogo) {
		this.nomeLuogo = nomeLuogo;
	}


	public String getDescrizioneLuogo() {
		return descrizioneLuogo;
	}


	public void setDescrizioneLuogo(String descrizioneLuogo) {
		this.descrizioneLuogo = descrizioneLuogo;
	}


	public String getProprietaFazione() {
		return proprietaFazione;
	}


	public void setProprietaFazione(String proprietaFazione) {
		this.proprietaFazione = proprietaFazione;
	}


	public String getStatoLuogo() {
		return statoLuogo;
	}


	public void setStatoLuogo(String statoLuogo) {
		this.statoLuogo = statoLuogo;
	}


	public double getLatNE() {
		return latNE;
	}


	public void setLatNE(double latNE) {
		this.latNE = latNE;
	}


	public double getLngNE() {
		return lngNE;
	}


	public void setLngNE(double lngNE) {
		this.lngNE = lngNE;
	}


	public double getLatSO() {
		return latSO;
	}


	public void setLatSO(double latSO) {
		this.latSO = latSO;
	}


	public double getLngSO() {
		return lngSO;
	}


	public void setLngSO(double lngSO) {
		this.lngSO = lngSO;
	}

}